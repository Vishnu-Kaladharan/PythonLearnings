# All about Strings

# Using ' or " in a string
print("Hello's ")
print('They"ll')

# Using a multi line string using three quotes
print('''Hello,
My name is Vishnu
Thanks & Regards,
Vishnu''')

sample_string = 'Hello World'
# Get the index of string
print(sample_string[0])

# Negative Index to get reverse index
print(sample_string[-2])

# get sub string using colon
print(sample_string[0:3])
print(sample_string[:3])
print(sample_string[3:])
print(sample_string[:])
